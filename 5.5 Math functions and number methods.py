# The round function
print(round(2.5))
print(round(7.5))

# Rounding a number to a given number of decimal places
print(round(3.21412423, 3))

# The abs() function
print(abs(3))
print(abs(-3.4))

# The pow() function
print(pow(2, 3))
print(pow(2, -2))
print(pow(2, 3, 2))  # (x, y, z) x = base, y = exponent z = finding remainder (reszta)